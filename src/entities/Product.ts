const assignImagesToProducts = (
  products: Product[],
  images: string[]
): void => {
  const numberOfImagesPerProduct = 4;
  let imageIndex = 0;

  products.forEach((product) => {
    const productImages = images.slice(
      imageIndex,
      imageIndex + numberOfImagesPerProduct
    );
    product.images = productImages;
    imageIndex += numberOfImagesPerProduct;
  });
};

